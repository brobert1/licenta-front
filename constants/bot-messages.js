export const getMessage = (triggerType, usedMessages = new Set()) => {
  const messages = botMessages[triggerType];
  if (!messages || isEmpty(messages)) {
    return null;
  }

  const availableMessages = difference(messages, Array.from(usedMessages));

  return sample(isEmpty(availableMessages) ? messages : availableMessages);
};
